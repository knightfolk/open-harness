const withGlm52 = filterAutoGeneratedGlmRouterCandidates([
  candidate('z-ai-zhipu:glm-5.2'),
  candidate('z-ai-zhipu:glm-5.1'),
  candidate('z-ai-zhipu:glm-5'),
  candidate('minimax:MiniMax-M3'),
]);
assert.deepEqual(
  withGlm52.map((item) => item.modelId),
  ['z-ai-zhipu:glm-5.2', 'minimax:MiniMax-M3'],
  'Auto-generated older same-provider GLM-5 candidates should be suppressed when GLM-5.2 is configured',
);
