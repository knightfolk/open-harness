assert.equal(
  pickPreferredGlm52RouterCandidate([
    candidate('z-ai-zhipu:glm-5.1'),
    candidate('z-ai-zhipu:glm-5.2'),
    candidate('minimax:MiniMax-M3'),
  ]),
  'z-ai-zhipu:glm-5.2',
  'Settings should prefer the configured GLM-5.2 candidate when it is present but not first',
);
