function candidate(modelId: string): AutoRouterCandidateConfig {
  return {
    modelId,
    cost: 0.2,
    supportsImages: false,
    supportsThinking: true,
    card: `${modelId} card`,
  };
}

function candidateWithCard(modelId: string, card: string): AutoRouterCandidateConfig {
  return {
    ...candidate(modelId),
    card,
  };
}

