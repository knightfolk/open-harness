const normalize = (value: string) => value.toLowerCase().replace(/[^a-z0-9]+/g, '');

export function normalizeModelCatalogKey(value: string): string {
  return normalize(value);
}

export function findModelCatalogCard(modelId?: string | null, providerId = ''): ModelCatalogCard | undefined {
  if (!modelId) return undefined;
  const bareId = modelId.includes(':') ? modelId.split(':').slice(1).join(':') : modelId;
  const normalizedModel = normalize(bareId);
  const normalizedProvider = normalize(providerId);

  return TOP_MODEL_CATALOG.find((card) => {
    const modelKeys = [card.id, card.displayName, ...card.aliases].map(normalize);
    const providerMatches =
      !normalizedProvider ||
      card.providerHints.some((hint) => normalizedProvider.includes(normalize(hint))) ||
      normalize(card.provider).includes(normalizedProvider);
    return providerMatches && modelKeys.some((key) => normalizedModel === key || normalizedModel.includes(key) || key.includes(normalizedModel));
  }) || TOP_MODEL_CATALOG.find((card) => {
    const modelKeys = [card.id, card.displayName, ...card.aliases].map(normalize);
    return modelKeys.some((key) => normalizedModel === key || normalizedModel.includes(key) || key.includes(normalizedModel));
  });
}

export function modelCatalogTooltip(modelId?: string | null, providerId = ''): string {
  const card = findModelCatalogCard(modelId, providerId);
  if (!card) return 'No catalog card matched yet. Add one in src/data/modelCatalog.ts when this model becomes a supported default.';
  const cost = card.inputCostPerMTok != null && card.outputCostPerMTok != null
    ? `$${card.inputCostPerMTok}/$${card.outputCostPerMTok} per 1M tokens`
    : `${card.relativeCost} relative cost`;
  return `${card.displayName}: ${card.compactDescription} Cost: ${cost}. Comparable to ${card.comparableTo.join(', ')}.`;
}

export function modelCatalogSummary(modelId?: string | null, providerId = ''): string | null {
  return findModelCatalogCard(modelId, providerId)?.compactDescription || null;
}

export function formatContextWindow(tokens: number): string {
  if (tokens >= M) {
    const millions = tokens / M;
    return `${Number.isInteger(millions) ? millions.toFixed(0) : millions.toFixed(1)}M`;
  }
  return `${Math.round(tokens / 1000)}K`;
}

export function formatModelCost(card: ModelCatalogCard): string {
  if (card.inputCostPerMTok == null || card.outputCostPerMTok == null) return card.relativeCost;
  return `$${card.inputCostPerMTok}/$${card.outputCostPerMTok}`;
}

export function modelBestCategory(card: ModelCatalogCard): ModelBestCategory {
  if (card.bestCategory) return card.bestCategory;
  const text = [
    card.compactDescription,
    ...card.strengths,
    ...card.bestFor,
  ].join(' ').toLowerCase();

  if (text.includes('rag') || text.includes('retrieval')) return 'rag';
  if (text.includes('local') || text.includes('open-weight') || text.includes('open deployment')) return 'local';
  if (text.includes('image') || text.includes('video') || text.includes('multimodal') || text.includes('visual')) return 'multimodal';
  if (text.includes('long context') || text.includes('large repo') || text.includes('repo scan') || text.includes('massive context')) return 'long-context';
  if (text.includes('coding') || text.includes('code generation') || text.includes('implementation') || text.includes('repo edits') || text.includes('refactor')) return 'coding';
  if (text.includes('reason') || text.includes('planning') || text.includes('planner') || text.includes('analysis')) return 'reasoning';
  if (text.includes('review') || text.includes('audit')) return 'review';
  if (text.includes('worker') || text.includes('summary') || text.includes('classification') || text.includes('tool call')) return 'worker';
  return 'coding';
}
